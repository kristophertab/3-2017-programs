static void All_Config(void)
{
	GPIO_InitTypeDef gpio;
	SPI_InitTypeDef  spi;

	SysTick_Config(SystemCoreClock / 1000);
	/* Enable the SPI clock */
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_SPI1, ENABLE);
	/* Enable the GPIOA clock */
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOA, ENABLE);
	/* Connect PA5 to SPI1_SCK */
	GPIO_PinAFConfig(GPIOA, GPIO_PinSource5, GPIO_AF_SPI1);
	/* Connect PA6 to SPI1_MISO */
	GPIO_PinAFConfig(GPIOA, GPIO_PinSource6, GPIO_AF_SPI1);
	/* Connect PA7 to SPI1_MOSI */
	GPIO_PinAFConfig(GPIOA, GPIO_PinSource7, GPIO_AF_SPI1);
	/* Configure SPI1 pins as alternate function (No need to
	configure PA4 since NSS will be managed by software) */

	 GPIO_StructInit(&gpio);
	 gpio.GPIO_Pin = GPIO_Pin_5|GPIO_Pin_7; // SCK, MOSI
	 gpio.GPIO_Mode = GPIO_Mode_AF;
	 gpio.GPIO_Speed = GPIO_Speed_50MHz; //speed
	 GPIO_Init(GPIOA, &gpio);

	 gpio.GPIO_Pin = GPIO_Pin_6; // MISO
	 gpio.GPIO_Mode = GPIO_Mode_IN;
	 GPIO_Init(GPIOA, &gpio);

	 gpio.GPIO_Pin = LCD_DC|LCD_CE|LCD_RST;
	 gpio.GPIO_Mode = GPIO_Mode_OUT;
	 GPIO_Init(GPIOC, &gpio);
	 GPIO_SetBits(GPIOC, LCD_CE|LCD_RST);

//	//manual:
//	gpio.GPIO_Pin =  GPIO_Pin_5|GPIO_Pin_6|GPIO_Pin_7;
//	gpio.GPIO_Mode = GPIO_Mode_AF;
//	gpio.GPIO_Speed = GPIO_Speed_50MHz;
//	gpio.GPIO_OType = GPIO_OType_PP;
//	gpio.GPIO_PuPd = GPIO_PuPd_UP;
//	GPIO_Init(GPIOA, &gpio);
	/* SPI configuration *****************************************/
//	spi.SPI_Direction = SPI_Direction_2Lines_FullDuplex; ???
	spi.SPI_Mode = SPI_Mode_Master;
//	spi.SPI_DataSize = SPI_DataSize_8b;
//	spi.SPI_CPOL = SPI_CPOL_High;
//	spi.SPI_CPHA = SPI_CPHA_2Edge;
	spi.SPI_NSS = SPI_NSS_Soft;
	spi.SPI_BaudRatePrescaler = SPI_BaudRatePrescaler_16;
//	spi.SPI_FirstBit = SPI_FirstBit_MSB;
//	spi.SPI_CRCPolynomial = 7;
	SPI_Init(SPI1, &spi);
	SPI_Cmd(SPI1, ENABLE);
}

