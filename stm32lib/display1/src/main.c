#define LCD_DC	GPIO_Pin_1
#define LCD_CE	GPIO_Pin_2
#define LCD_RST	GPIO_Pin_3

volatile uint32_t timer_ms = 0;

void SysTick_Handler()
{
 if (timer_ms) {
 timer_ms--;
 }
}

void delay_ms(int time)
{
 timer_ms = time;
 while (timer_ms) {};
}

void lcd_reset(){
	GPIO_ResetBits(GPIOA, LCD_RST); //100 ns delay between, should be...
	delay_ms(1);
	GPIO_SetBits(GPIOA, LCD_RST);
}

void lcd_cmd(uint8_t cmd){
	GPIO_ResetBits(GPIOA, LCD_CE|LCD_DC);
	while (SPI_I2S_GetFlagStatus(SPI1, SPI_I2S_FLAG_TXE) == RESET);
	SPI_SendData(SPI1, cmd);
	GPIO_SetBits(GPIOA, LCD_CE);
}

void lcd_data(const uint8_t* data, int size){
	int i;
	GPIO_SetBits(GPIOA, LCD_DC);
	GPIO_ResetBits(GPIOA, LCD_CE);
	for(i=0;i<size;i++) {
		while (SPI_I2S_GetFlagStatus(SPI1, SPI_I2S_FLAG_TXE) == RESET);
		SPI_SendData(SPI1, data[i]);
	}
	GPIO_SetBits(GPIOA, LCD_CE);
}

